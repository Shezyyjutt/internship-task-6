export function buildCourseCatalog(realCourses = []) {
  if (realCourses.length >= demoCourses.length) {
    return realCourses.map((course) => ({ ...course, isSample: false }));
  }

  const sampleCourses = demoCourses
    .slice(0, demoCourses.length - realCourses.length)
    .map((course) => ({ ...course, isSample: true }));

  return [
    ...realCourses.map((course) => ({ ...course, isSample: false })),
    ...sampleCourses,
  ];
}

export function getCatalogStats(realCourses = []) {
  const catalogCourses = buildCourseCatalog(realCourses);
  const realInstructorKeys = realCourses
    .map((course) => course.instructor?._id || course.instructor?.name)
    .filter(Boolean);

  return {
    courses: catalogCourses.length,
    students: demoStudents.length,
    instructors: Math.max(
      demoInstructors.length,
      new Set([...realInstructorKeys, ...demoInstructors.map((instructor) => instructor.id)]).size
    ),
    hasSampleData: catalogCourses.some((course) => course.isSample),
  };
}

